package operadores;

public class Operadores {
    public static void main(String[] args) {
        /*OPERADORES ARITMETICOS
        double suma, resta, multiplicacion, division, modulo;
        double a = 25;
        double b = 3;

        suma = a + b;
        System.out.println("suma = " + suma);
        resta = a - b;
        System.out.println("resta = " + resta);
        multiplicacion = a*b;
        System.out.println("multiplicacion = " + multiplicacion);
        division = a/b;
        System.out.println("division = " + division);
        modulo = a%b;
        System.out.println("modulo = " + modulo);
        */


        /*RELACIONALES
        int numero1 = 20;
        int numero2 = 30;

        boolean esIgual = numero1 == numero2;
        System.out.println("esIgual = " + esIgual);
        boolean esDiferente = numero1 != numero2;
        System.out.println("esDiferente = " + esDiferente);
        boolean esMayor = numero1 > numero2;
        System.out.println("esMayor = " + esMayor);
        boolean esMenor = numero1 < numero2;
        System.out.println("esMenor = " + esMenor);
        boolean esMayorIgual = numero1 >= numero2;
        System.out.println("esMayorIgual = " + esMayorIgual);
        boolean esMenorIgual = numero1 <= numero2;
        System.out.println("esMenorIgual = " + esMenorIgual);

        */


        //RELACIONALES
        boolean a = true, b = false;

        boolean and = a && b;
        System.out.println("and = " + and);
        boolean or = a || b;
        System.out.println("or = " + or);
        boolean negationA = !a;
        System.out.println("negationA = " + negationA);
        boolean negationB = !b;
        System.out.println("negationB = " + negationB);




    }
}
